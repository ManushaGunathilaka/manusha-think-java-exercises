package ArryaListVsLinkedList;

import java.util.ArrayList;
import java.util.LinkedList;

public class LinkedListExample {

    public static void main(String[] args) {

        LinkedList<String> namesLinkedList = new LinkedList<>();
        namesLinkedList.add("Manusha");
        namesLinkedList.add("Priyan");
        namesLinkedList.add("Malinda");
        namesLinkedList.add("Gunathilaka");

        ArrayList<String> nameArrayList = new ArrayList<>();
        nameArrayList.add("Manusha");
        nameArrayList.add("Priyan");
        nameArrayList.add("Malinda");
        nameArrayList.add("Gunathilaka");
    }
}
